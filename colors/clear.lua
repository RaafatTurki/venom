--- clears defualt nvim highlight groups
-- @module hl_clear

local M = {}

M.clear_hl = function(group_name, opts)
  vim.cmd([[hi ]] .. group_name .. [[ NONE]])
end

M.clear = function()
  for hl_group, opts in pairs(M.highlights) do
    M.clear_hl(hl_group, opts)
  end
end

